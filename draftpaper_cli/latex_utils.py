from __future__ import annotations

from typing import Any


LATEX_REPLACEMENTS = {
    "\\": r"\textbackslash{}",
    "&": r"\&",
    "%": r"\%",
    "$": r"\$",
    "#": r"\#",
    "_": r"\_",
    "{": r"\{",
    "}": r"\}",
    "~": r"\textasciitilde{}",
    "^": r"\textasciicircum{}",
}


def safe_latex_text(text: Any) -> str:
    return "".join(LATEX_REPLACEMENTS.get(char, char) for char in str(text or ""))
